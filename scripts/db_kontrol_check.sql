SELECT
    rc.TableName,
    rc.ColumnName,
    rc.ExpectedType,
    rc.ExpectedValues,
    CASE WHEN c.column_id IS NULL THEN 'MISSING' ELSE 'OK' END AS ColumnStatus,
    t.name AS ActualType,
    c.max_length,
    c.is_nullable
FROM @RequiredColumns rc
LEFT JOIN sys.tables st
    ON st.name = rc.TableName
LEFT JOIN sys.schemas ss
    ON ss.schema_id = st.schema_id
    AND ss.name = @SchemaName
LEFT JOIN sys.columns c
    ON c.object_id = st.object_id
    AND c.name = rc.ColumnName
LEFT JOIN sys.types t
    ON t.user_type_id = c.user_type_id
ORDER BY rc.TableName, rc.ColumnName;

PRINT '=== STEP 3: Value checks (K_DAHIKOD_AYARLAR) ===';

IF OBJECT_ID(QUOTENAME(@SchemaName) + '.[K_DAHIKOD_AYARLAR]', 'U') IS NOT NULL
BEGIN
    SELECT TOP 1 *
    FROM K_DAHIKOD_AYARLAR;

    SELECT
        CASE WHEN BILDIRIM_GONDERME_DENEME_SAYISI IS NULL OR BILDIRIM_GONDERME_DENEME_SAYISI <= 0 THEN 'INVALID' ELSE 'OK' END AS BILDIRIM_GONDERME_DENEME_SAYISI,
        CASE WHEN KAC_DAKIKA_ONCEKI_BILDIRIM_GONDERILSIN IS NULL OR KAC_DAKIKA_ONCEKI_BILDIRIM_GONDERILSIN <= 0 THEN 'INVALID' ELSE 'OK' END AS KAC_DAKIKA_ONCEKI_BILDIRIM_GONDERILSIN,
