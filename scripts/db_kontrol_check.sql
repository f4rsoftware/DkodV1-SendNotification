        CASE WHEN UPPER(ISNULL(AKTIF_BIRINCI_FIRMA,'')) = UPPER(ISNULL(AKTIF_IKINCI_FIRMA,'')) THEN 'INVALID_SAME_FIRMA' ELSE 'OK' END AS AKTIF_FIRMA_COMBINATION
    FROM K_DAHIKOD_AYARLAR;
END
ELSE
BEGIN
    PRINT 'K_DAHIKOD_AYARLAR table is missing; value checks skipped.';
END;

