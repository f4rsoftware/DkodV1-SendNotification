    t.TableName,
    CASE WHEN OBJECT_ID(QUOTENAME(@SchemaName) + '.' + QUOTENAME(t.TableName), 'U') IS NULL THEN 'MISSING' ELSE 'OK' END AS TableStatus
FROM @RequiredTables t
ORDER BY t.TableName;

