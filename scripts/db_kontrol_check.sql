        CASE WHEN NET_GSM_BIRINCI_FIRMA NOT IN (0,1) THEN 'INVALID' ELSE 'OK' END AS NET_GSM_BIRINCI_FIRMA,
        CASE WHEN KOBICOM_BIRINCI_FIRMA NOT IN (0,1) THEN 'INVALID' ELSE 'OK' END AS KOBICOM_BIRINCI_FIRMA,
        CASE WHEN KIRMIZI_SANTRAL_BIRINCI_FIRMA NOT IN (0,1) THEN 'INVALID' ELSE 'OK' END AS KIRMIZI_SANTRAL_BIRINCI_FIRMA,
        CASE WHEN CAGRI_SONLANDIRMA_GERI_ARAMASI NOT IN (0,1) THEN 'INVALID' ELSE 'OK' END AS CAGRI_SONLANDIRMA_GERI_ARAMASI,
        CASE WHEN CAGRI_SONLANDIRMA_ARAMA_BASLAMA_DAKIKASI IS NULL OR CAGRI_SONLANDIRMA_ARAMA_BASLAMA_DAKIKASI < 0 THEN 'INVALID' ELSE 'OK' END AS CAGRI_SONLANDIRMA_ARAMA_BASLAMA_DAKIKASI,
